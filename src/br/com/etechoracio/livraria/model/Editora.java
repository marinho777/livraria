package br.com.etechoracio.livraria.model;

public class Editora {
    private String nome, site;

    public void setNome(String nome) {
        this.nome = nome;
    }
    public void setSite(String site) {
        this.site = site;
    }

    public String getNome() {
        return nome;
    }

    public String getSite() {
        return site;
    }
}
